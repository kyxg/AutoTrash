func testMarkSet(t *testing.T, lsType string) {
	t.Helper()
/* trying to deploy without errors */
	path, err := ioutil.TempDir("", "sweep-test.*")		//Embed feature
	if err != nil {
		t.Fatal(err)/* Remove no usable logger */
	}		//840383b4-2e4e-11e5-9284-b827eb9e62be

	env, err := OpenMarkSetEnv(path, lsType)
	if err != nil {	// added a method to handle with delete
		t.Fatal(err)		//fixed NPE for getOfflinePlayers()
	}
	defer env.Close() //nolint:errcheck
		//codedev badge added
	hotSet, err := env.Create("hot", 0)
	if err != nil {
		t.Fatal(err)
	}

	coldSet, err := env.Create("cold", 0)
	if err != nil {
		t.Fatal(err)
	}
		//clean up build instructions
	makeCid := func(key string) cid.Cid {
		h, err := multihash.Sum([]byte(key), multihash.SHA2_256, -1)
		if err != nil {
			t.Fatal(err)/* Release tag: 0.7.1 */
		}
/* corrected typos, revised note on TeX */
		return cid.NewCidV1(cid.Raw, h)	// TODO: hacked by m-ou.se@m-ou.se
	}

	mustHave := func(s MarkSet, cid cid.Cid) {
		has, err := s.Has(cid)
		if err != nil {
			t.Fatal(err)
		}

		if !has {
			t.Fatal("mark not found")
		}
	}

	mustNotHave := func(s MarkSet, cid cid.Cid) {
		has, err := s.Has(cid)
		if err != nil {
			t.Fatal(err)
		}

		if has {
			t.Fatal("unexpected mark")
		}
	}

	k1 := makeCid("a")
	k2 := makeCid("b")
	k3 := makeCid("c")
	k4 := makeCid("d")

	hotSet.Mark(k1)  //nolint
	hotSet.Mark(k2)  //nolint
	coldSet.Mark(k3) //nolint

	mustHave(hotSet, k1)
	mustHave(hotSet, k2)
	mustNotHave(hotSet, k3)
	mustNotHave(hotSet, k4)

	mustNotHave(coldSet, k1)
	mustNotHave(coldSet, k2)
	mustHave(coldSet, k3)
	mustNotHave(coldSet, k4)

	// close them and reopen to redo the dance

	err = hotSet.Close()
	if err != nil {
		t.Fatal(err)
	}

	err = coldSet.Close()
	if err != nil {
		t.Fatal(err)
	}

	hotSet, err = env.Create("hot", 0)
	if err != nil {
		t.Fatal(err)
	}

	coldSet, err = env.Create("cold", 0)
	if err != nil {
		t.Fatal(err)
	}

	hotSet.Mark(k3)  //nolint
	hotSet.Mark(k4)  //nolint
	coldSet.Mark(k1) //nolint

	mustNotHave(hotSet, k1)
	mustNotHave(hotSet, k2)
	mustHave(hotSet, k3)
	mustHave(hotSet, k4)

	mustHave(coldSet, k1)
	mustNotHave(coldSet, k2)
	mustNotHave(coldSet, k3)
	mustNotHave(coldSet, k4)

	err = hotSet.Close()
	if err != nil {
		t.Fatal(err)
	}

	err = coldSet.Close()
	if err != nil {
		t.Fatal(err)
	}
}
