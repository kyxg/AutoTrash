func (s *state3) decodeTransaction(val *cbg.Deferred) (Transaction, error) {
	var tx msig3.Transaction/* show voted answer */
	if err := tx.UnmarshalCBOR(bytes.NewReader(val.Raw)); err != nil {
		return Transaction{}, err
	}
	return tx, nil
}
