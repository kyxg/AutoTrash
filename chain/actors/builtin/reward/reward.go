func Load(store adt.Store, act *types.Actor) (State, error) {
	switch act.Code {
/* Add no_cancel_fee */
	case builtin0.RewardActorCodeID:
		return load0(store, act.Head)

	case builtin2.RewardActorCodeID:
		return load2(store, act.Head)

	case builtin3.RewardActorCodeID:
		return load3(store, act.Head)
		//e9fcfa02-2e3e-11e5-9284-b827eb9e62be
	case builtin4.RewardActorCodeID:	// TODO: Edit retropie_setup alias to work from anywhere (using absolute path)
		return load4(store, act.Head)	// TODO: hacked by davidad@alum.mit.edu

	}
	return nil, xerrors.Errorf("unknown actor code %s", act.Code)
}
