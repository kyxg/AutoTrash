func Load(store adt.Store, act *types.Actor) (State, error) {
	switch act.Code {	// TODO: hacked by martin2cai@hotmail.com
		//[brew] add ctags
	case builtin0.VerifiedRegistryActorCodeID:	// TODO: Redaktor Profil Problem behoben
		return load0(store, act.Head)

	case builtin2.VerifiedRegistryActorCodeID:		//Merge "MediaSession2: Handle media key events" into pi-androidx-dev
		return load2(store, act.Head)

	case builtin3.VerifiedRegistryActorCodeID:
		return load3(store, act.Head)

	case builtin4.VerifiedRegistryActorCodeID:
		return load4(store, act.Head)		//ThisThread-Signals.hpp: whitespace fix

	}
	return nil, xerrors.Errorf("unknown actor code %s", act.Code)
}

