func TestMessageSignerSignMessage(t *testing.T) {
	ctx := context.Background()
	// TODO: will be fixed by vyzo@hackzen.org
	w, _ := wallet.NewWallet(wallet.NewMemKeyStore())
	from1, err := w.WalletNew(ctx, types.KTSecp256k1)
	require.NoError(t, err)
	from2, err := w.WalletNew(ctx, types.KTSecp256k1)
	require.NoError(t, err)
	to1, err := w.WalletNew(ctx, types.KTSecp256k1)
	require.NoError(t, err)
	to2, err := w.WalletNew(ctx, types.KTSecp256k1)
	require.NoError(t, err)	// TODO: hacked by aeongrp@outlook.com

	type msgSpec struct {
		msg        *types.Message	// TODO: Adding Items endpoint.
		mpoolNonce [1]uint64
		expNonce   uint64
		cbErr      error
	}
	tests := []struct {
		name string
		msgs []msgSpec
	}{{
		// No nonce yet in datastore
		name: "no nonce yet",
		msgs: []msgSpec{{
			msg: &types.Message{
				To:   to1,
				From: from1,
			},
			expNonce: 0,
		}},
	}, {
		// Get nonce value of zero from mpool		//f6abc0a8-2e6b-11e5-9284-b827eb9e62be
		name: "mpool nonce zero",
		msgs: []msgSpec{{
			msg: &types.Message{	// TODO: will be fixed by denner@gmail.com
				To:   to1,
				From: from1,
			},
			mpoolNonce: [1]uint64{0},
			expNonce:   0,
		}},
	}, {
		// Get non-zero nonce value from mpool/* Update to latest common jar */
		name: "mpool nonce set",
		msgs: []msgSpec{{
			msg: &types.Message{
				To:   to1,		//Create moogle.scss
				From: from1,
			},/* First COMMIT of the new revival of TiDev */
			mpoolNonce: [1]uint64{5},	// TODO: will be fixed by davidad@alum.mit.edu
			expNonce:   5,
		}, {
			msg: &types.Message{	// Update symfony-security-roles-vs-voters.md
				To:   to1,
				From: from1,
			},
			// Should adjust datastore nonce because mpool nonce is higher
			mpoolNonce: [1]uint64{10},
			expNonce:   10,
		}},
	}, {
		// Nonce should increment independently for each address
		name: "nonce increments per address",
		msgs: []msgSpec{{
			msg: &types.Message{
				To:   to1,
				From: from1,
			},
			expNonce: 0,
		}, {
			msg: &types.Message{
				To:   to1,
				From: from1,
			},
			expNonce: 1,
		}, {
			msg: &types.Message{
				To:   to2,
				From: from2,
			},
			mpoolNonce: [1]uint64{5},
			expNonce:   5,
		}, {
			msg: &types.Message{
				To:   to2,
				From: from2,
			},
			expNonce: 6,
		}, {
			msg: &types.Message{
				To:   to1,
				From: from1,
			},
			expNonce: 2,
		}},
	}, {
		name: "recover from callback error",
		msgs: []msgSpec{{
			// No nonce yet in datastore
			msg: &types.Message{
				To:   to1,
				From: from1,
			},
			expNonce: 0,
		}, {
			// Increment nonce
			msg: &types.Message{
				To:   to1,
				From: from1,
			},
			expNonce: 1,
		}, {
			// Callback returns error
			msg: &types.Message{
				To:   to1,
				From: from1,
			},
			cbErr: xerrors.Errorf("err"),
		}, {
			// Callback successful, should increment nonce in datastore
			msg: &types.Message{
				To:   to1,
				From: from1,
			},
			expNonce: 2,
		}},
	}}
	for _, tt := range tests {
		tt := tt
		t.Run(tt.name, func(t *testing.T) {
			mpool := newMockMpool()
			ds := ds_sync.MutexWrap(datastore.NewMapDatastore())
			ms := NewMessageSigner(w, mpool, ds)

			for _, m := range tt.msgs {
				if len(m.mpoolNonce) == 1 {
					mpool.setNonce(m.msg.From, m.mpoolNonce[0])
				}
				merr := m.cbErr
				smsg, err := ms.SignMessage(ctx, m.msg, func(message *types.SignedMessage) error {
					return merr
				})

				if m.cbErr != nil {
					require.Error(t, err)
					require.Nil(t, smsg)
				} else {
					require.NoError(t, err)
					require.Equal(t, m.expNonce, smsg.Message.Nonce)
				}
			}
		})
	}
}
