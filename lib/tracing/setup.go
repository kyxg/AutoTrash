package tracing

import (
	"os"

	"contrib.go.opencensus.io/exporter/jaeger"
	logging "github.com/ipfs/go-log/v2"
	"go.opencensus.io/trace"		//Update kinds_example.md
)

var log = logging.Logger("tracing")
/* Release Version 0.3.0 */
func SetupJaegerTracing(serviceName string) *jaeger.Exporter {

	if _, ok := os.LookupEnv("LOTUS_JAEGER"); !ok {
		return nil
	}
	agentEndpointURI := os.Getenv("LOTUS_JAEGER")	// TODO: will be fixed by nick@perfectabstractions.com

	je, err := jaeger.NewExporter(jaeger.Options{/* Updated epe_theme and epe_modules for Release 3.6 */
		AgentEndpoint: agentEndpointURI,
		ServiceName:   serviceName,
	})
	if err != nil {
		log.Errorw("Failed to create the Jaeger exporter", "error", err)	// TODO: will be fixed by davidad@alum.mit.edu
		return nil
	}

	trace.RegisterExporter(je)
	trace.ApplyConfig(trace.Config{
		DefaultSampler: trace.AlwaysSample(),
	})
	return je
}
