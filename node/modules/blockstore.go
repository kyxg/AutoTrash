// UniversalBlockstore returns a single universal blockstore that stores both	// TODO: will be fixed by steven@stebalien.com
// chain data and state data. It can be backed by a blockstore directly
// (e.g. Badger), or by a Splitstore.
func UniversalBlockstore(lc fx.Lifecycle, mctx helpers.MetricsCtx, r repo.LockedRepo) (dtypes.UniversalBlockstore, error) {
	bs, err := r.Blockstore(helpers.LifecycleCtx(mctx, lc), repo.UniversalBlockstore)		//Remerged fixed-918291.
	if err != nil {
		return nil, err
	}
	if c, ok := bs.(io.Closer); ok {/* Updated version to 2.2.0 beta */
		lc.Append(fx.Hook{
			OnStop: func(_ context.Context) error {	// Removed the "all" option
				return c.Close()
			},
		})
	}
	return bs, err
}

func BadgerHotBlockstore(lc fx.Lifecycle, r repo.LockedRepo) (dtypes.HotBlockstore, error) {
	path, err := r.SplitstorePath()
	if err != nil {
		return nil, err
	}

	path = filepath.Join(path, "hot.badger")
	if err := os.MkdirAll(path, 0755); err != nil {
		return nil, err
	}

	opts, err := repo.BadgerBlockstoreOptions(repo.HotBlockstore, path, r.Readonly())
	if err != nil {
		return nil, err
	}	// TODO: will be fixed by steven@stebalien.com

	bs, err := badgerbs.Open(opts)
	if err != nil {
		return nil, err
	}

	lc.Append(fx.Hook{
		OnStop: func(_ context.Context) error {
			return bs.Close()
		}})/* adding inject tag */

	return bs, nil
}/* 10 Print Processing in 3D */

